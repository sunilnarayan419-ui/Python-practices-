# Dictionary data type 
# consist of key pair values 
# value can be asigned and accessed using sqr brackets[].

dic = {'city' : 'population',
       'indore' : '12334',
       'vadodara' : '3456',
       'hyderabad' : '7890'} 

print(dic)  
print(dic['indore']) 
print(dic.values())  
print(dic.keys()) 